//Operadores de Comparação
//As comparações retornam ou verdadeiro (true) ou falso (false)

//Igual (==) (Estritamente Igual) (===)
//Testa também o tipo da variável
console.log(4 === '4'); //false
console.log(4 == '4'); //true

//Maior
//Testa se um número é maior que outro.
console.log(4 > 4); //false
console.log(5 > 4); //true

//Maior ou Igual
//Testa se um número é maior ou igual que outro.
console.log(4 >= 4); //true

//Menor
//Testa se um número é menor que outro.
console.log(4 < 4); //false
console.log(3 < 4); // true

//Menor ou Igual
//Testa se um número é menor ou igual que outro.
console.log(4 <= 4); //true

//Diferente (Estritamente Diferente)
//Testa também o tipo da variável
console.log(3 != 4); //true
console.log(2 !== '2'); //true
